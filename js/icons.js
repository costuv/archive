/**
 * Get icon by name
 * @param {string} name - Icon name
 * @returns {string} SVG string
 */
function getIcon(name) {
  return Icons[name] || Icons['alert-circle'];
}

/**
 * Get icon for file type
 * @param {string} type - File type (document, pdf, image, notice)
 * @returns {string} SVG string
 */
function getFileTypeIcon(type) {
  const typeIcons = {
    'document': Icons['file-text'],
    'pdf': Icons['file-type'],
    'image': Icons['image'],
    'audio': Icons['music'],
    'video': Icons['video'],
    'codes': Icons['code'],
    'notice': Icons['alert-circle']
  };
  return typeIcons[type] || Icons['file-text'];
}

/**
 * Initialize all icon placeholders in the DOM
 * Call this after rendering new content
 */
function initializeIcons() {
  document.querySelectorAll('.icon[data-icon]').forEach(el => {
    const iconName = el.getAttribute('data-icon');
    if (iconName && Icons[iconName]) {
      el.innerHTML = Icons[iconName];
    }
  });
}

